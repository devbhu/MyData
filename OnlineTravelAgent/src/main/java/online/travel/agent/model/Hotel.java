package online.travel.agent.model;

public class Hotel {
	int hotelId;
	int hotelCityId;
	int hotelCountryId;
	String hotelName;
	String hotelAddress;
	String hotelDiscription;
	int hotelMobNo;
	String emailId;
	int telephoneNo;
	String hotelAmenities ;
	int latitude;
	int longitude;
	
	public int getHotelId() {
		return hotelId;
	}
	public void setHotelId(int hotelId) {
		this.hotelId = hotelId;
	}
	public int getHotelCityId() {
		return hotelCityId;
	}
	public void setHotelCityId(int hotelCityId) {
		this.hotelCityId = hotelCityId;
	}
	public int getHotelCountryId() {
		return hotelCountryId;
	}
	public void setHotelCountryId(int hotelCountryId) {
		this.hotelCountryId = hotelCountryId;
	}
	public String getHotelName() {
		return hotelName;
	}
	public void setHotelName(String hotelName) {
		this.hotelName = hotelName;
	}
	public String getHotelAddress() {
		return hotelAddress;
	}
	public void setHotelAddress(String hotelAddress) {
		this.hotelAddress = hotelAddress;
	}
	public String getHotelDiscription() {
		return hotelDiscription;
	}
	public void setHotelDiscription(String hotelDiscription) {
		this.hotelDiscription = hotelDiscription;
	}
	public int getHotelMobNo() {
		return hotelMobNo;
	}
	public void setHotelMobNo(int hotelMobNo) {
		this.hotelMobNo = hotelMobNo;
	}
	public String getEmailId() {
		return emailId;
	}
	public void setEmailId(String emailId) {
		this.emailId = emailId;
	}
	public int getTelephoneNo() {
		return telephoneNo;
	}
	public void setTelephoneNo(int telephoneNo) {
		this.telephoneNo = telephoneNo;
	}
	public String getHotelAmenities() {
		return hotelAmenities;
	}
	public void setHotelAmenities(String hotelAmenities) {
		this.hotelAmenities = hotelAmenities;
	}
	public int getLatitude() {
		return latitude;
	}
	public void setLatitude(int latitude) {
		this.latitude = latitude;
	}
	public int getLongitude() {
		return longitude;
	}
	public void setLongitude(int longitude) {
		this.longitude = longitude;
	}
	
	
}
